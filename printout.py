def display(file1, file2, graf1, graf2, score, score_type):
    print("\n================================================================================\n")
    print(score_type + ": " + str(score) + "\n")
    print(" --- " + file1 + " --- ")
    print(graf1 + "\n")
    print(" --- " + file2 + " --- ")
    print(graf2 + "\n\n")
